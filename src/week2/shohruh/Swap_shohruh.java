package week2.shohruh;

public class Swap_shohruh {
    public static void main(String[] args) {

        int a = 20;
        int b = 5;

        a = a + b; // a = 25

        b = a - b; // b = 5

        a = a - b;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

    }
}


