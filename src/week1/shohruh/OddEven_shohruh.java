package shohruh;

public class OddEven_shohruh {
    public static void main(String[] args) {
identify(7);
    }


    public static void identify(int number) {

        if (number % 2 == 0) {
            System.out.println(number + " is an even number");
        } else {
            System.out.println(number + " is an odd number");
        }


    }

}


